    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        RequestDispatcher dispatch = request.getRequestDispatcher("/views/SignIn.jsp");
        dispatch.forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String email = (String) request.getParameter("email");
        String password = (String) request.getParameter("password");

        UserModel user = new UserModel();
        user.setEmail(email);
        user.setPassword(password);

        UserController uc = new UserController();

        try {
            UserModel verifiedUser = uc.login(user);
            if (verifiedUser != null) {
                // TODO : ADD SESSION HERE
                HttpSession session = request.getSession();
                session.setAttribute("user", verifiedUser.getId());
                response.sendRedirect("./katalog");
            } else {
                response.sendRedirect("./login?status=notok");
            }
        } catch (SQLException ex) {
            Logger.getLogger(SigninServlet.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

